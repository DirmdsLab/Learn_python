x = int(20)
print(x,"Type :",type(x))

x = float(5.2)
print(x,"Type :",type(x))

x = complex(10j)
print(x,"Type :",type(x))

x = list(("Turu","Mending","Turuuu"))
print(x,"Type :",type(x))

x = tuple(("Turu","Mending","Turuuu"))
print(x,"Type :",type(x))

x = set(("Turu","Mending","Turuuu"))
print(x,"Type :",type(x))

x = frozenset(("Turu","Mending","Turuuu"))
print(x,"Type :",type(x))

