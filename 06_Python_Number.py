# int 
x = 1
y = 1234567890
z = -987654321

print(x,type(x))
print(y,type(y))
print(z,type(z))
print("Intinya bakal tetap int\n")

# float 
x = 1.5
y = 14.12345
z = -28.12345

print(x,type(x))
print(y,type(y))
print(z,type(z))
print("Intinya bakal tetap float\n")

# complex 
x = 10+5j
y = 10j
z = -10j

print(x,type(x))
print(y,type(y))
print(z,type(z))
print("Intinya bakal tetap complex\n")


